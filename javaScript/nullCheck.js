    console.log('0은 존재하지 않는 값입니다');
if(falseBoolean == null)
    console.log('false는 존재하지 않는 값입니다');
if(emptyString == null)
    console.log('빈 문자열은 존재하지 않는 값입니다');
if(undefinedValue == null)
    console.log('undefined는 존재하지 않는 값입니다');  // undefined는 존재하지 않는 값입니다
if(nullValue == null)
    console.log('null은 존재하지 않는 값입니다');       // null은 존재하지 않는 값입니다



